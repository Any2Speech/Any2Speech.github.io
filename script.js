function setupReveal() {
  const sections = document.querySelectorAll(".reveal");

  const showAll = () => sections.forEach((s) => s.classList.add("is-visible"));

  if (!("IntersectionObserver" in window)) {
    showAll();
    return;
  }

  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0 }
  );

  sections.forEach((section) => revealObserver.observe(section));

  setTimeout(showAll, 2500);
}

function setupNavSpy() {
  const navLinks = [...document.querySelectorAll('.site-nav a[href^="#"]')];
  const sectionIds = navLinks.map((link) => link.getAttribute("href").slice(1));
  const targets = sectionIds
    .map((id) => document.getElementById(id))
    .filter(Boolean);

  if (!targets.length) return;

  let currentId = "";
  const setActive = (id) => {
    if (id === currentId) return;
    currentId = id;
    navLinks.forEach((link) => {
      link.classList.toggle("is-active", link.getAttribute("href") === `#${id}`);
    });
  };

  let ticking = false;
  function onScroll() {
    if (ticking) return;
    ticking = true;
    requestAnimationFrame(() => {
      const atBottom = window.innerHeight + window.scrollY >= document.body.scrollHeight - 60;
      if (atBottom) {
        setActive(targets[targets.length - 1].id);
        ticking = false;
        return;
      }
      const triggerLine = window.scrollY + window.innerHeight * 0.3;
      let active = targets[0]?.id;
      for (const t of targets) {
        if (t.getBoundingClientRect().top + window.scrollY <= triggerLine) {
          active = t.id;
        }
      }
      setActive(active);
      ticking = false;
    });
  }

  window.addEventListener("scroll", onScroll, { passive: true });
  onScroll();
}

function setupContribHub() {
  const hub = document.querySelector('.contrib-hub');
  if (!hub) return;

  const cards = [...hub.querySelectorAll('.contrib-card[data-index]')];
  const panels = [...hub.querySelectorAll('.contrib-detail__panel[data-panel]')];
  const isCompact = () => matchMedia('(hover: none)').matches || matchMedia('(max-width: 960px)').matches;
  let current = 0;

  function activate(index) {
    if (index === current && cards[index].classList.contains('is-active')) return;
    current = index;
    hub.dataset.active = index;
    cards.forEach((c, i) => c.classList.toggle('is-active', i === index));
    panels.forEach((p, i) => p.classList.toggle('is-active', i === index));

    if (isCompact()) {
      panels[index]?.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }
  }

  cards.forEach((card) => {
    const idx = Number(card.dataset.index);

    card.addEventListener('mouseenter', () => {
      if (isCompact()) return;
      activate(idx);
    });

    card.addEventListener('click', (e) => {
      if (!isCompact()) return;
      e.preventDefault();
      activate(idx);
    });
  });
}

function formatTime(s) {
  if (!isFinite(s) || s < 0) return "0:00";
  const m = Math.floor(s / 60);
  const sec = Math.floor(s % 60);
  return `${m}:${sec.toString().padStart(2, "0")}`;
}

function setupAudioPlayers() {
  document.querySelectorAll(".audio-player").forEach((player) => {
    const audio = player.querySelector("audio");
    const btn = player.querySelector(".audio-player__btn");
    const seek = player.querySelector(".audio-player__seek");
    const fill = player.querySelector(".audio-player__fill");
    const bufferedEl = player.querySelector(".audio-player__buffered");
    const timeEl = player.querySelector(".audio-player__time");
    const originalSrc = audio.querySelector("source").getAttribute("src");
    let seeking = false;
    let loaded = false;

    async function ensureBlobLoaded() {
      if (loaded) return true;
      player.dataset.state = "loading";
      try {
        const resp = await fetch(originalSrc);
        if (!resp.ok) throw new Error(`HTTP ${resp.status}`);
        const blob = await resp.blob();
        const blobUrl = URL.createObjectURL(blob);
        audio.src = blobUrl;
        await new Promise((resolve, reject) => {
          audio.addEventListener("canplaythrough", resolve, { once: true });
          audio.addEventListener("error", reject, { once: true });
        });
        loaded = true;
        bufferedEl.style.width = "100%";
        return true;
      } catch (e) {
        console.error("Audio load failed:", e);
        player.dataset.state = "paused";
        return false;
      }
    }

    btn.addEventListener("click", async () => {
      if (audio.paused) {
        document.querySelectorAll(".audio-player audio").forEach((a) => {
          if (a !== audio && !a.paused) {
            a.pause();
            a.closest(".audio-player").dataset.state = "paused";
          }
        });
        if (!(await ensureBlobLoaded())) return;
        audio.play();
      } else {
        audio.pause();
      }
    });

    audio.addEventListener("play", () => { player.dataset.state = "playing"; });
    audio.addEventListener("pause", () => { player.dataset.state = "paused"; });

    audio.addEventListener("timeupdate", () => {
      if (seeking) return;
      const dur = audio.duration || 0;
      const pct = dur ? (audio.currentTime / dur) * 1000 : 0;
      seek.value = pct;
      fill.style.width = `${pct / 10}%`;
      timeEl.textContent = `${formatTime(audio.currentTime)} / ${formatTime(dur)}`;
    });

    audio.addEventListener("loadedmetadata", () => {
      timeEl.textContent = `0:00 / ${formatTime(audio.duration)}`;
    });

    audio.addEventListener("durationchange", () => {
      timeEl.textContent = `${formatTime(audio.currentTime)} / ${formatTime(audio.duration)}`;
    });

    seek.addEventListener("input", () => {
      seeking = true;
      const pct = seek.value / 1000;
      fill.style.width = `${pct * 100}%`;
      const dur = audio.duration || 0;
      if (dur) {
        timeEl.textContent = `${formatTime(pct * dur)} / ${formatTime(dur)}`;
      }
    });

    seek.addEventListener("change", async () => {
      const pct = seek.value / 1000;
      if (!loaded) {
        if (!(await ensureBlobLoaded())) {
          seeking = false;
          return;
        }
      }
      if (audio.duration) {
        audio.currentTime = pct * audio.duration;
      }
      seeking = false;
    });

    audio.addEventListener("ended", () => {
      player.dataset.state = "paused";
      seek.value = 0;
      fill.style.width = "0%";
    });
  });
}

function setupVideoPlayers() {
  document.querySelectorAll(".video-blob-wrap").forEach((wrap) => {
    const video = wrap.querySelector("video");
    const overlay = wrap.querySelector(".video-blob-wrap__overlay");
    const hint = wrap.querySelector(".video-blob-wrap__hint");
    const src = wrap.dataset.src;
    let loaded = false;

    async function loadAndPlay() {
      if (loaded) {
        video.play();
        return;
      }
      hint.textContent = "加载中…";
      overlay.classList.add("is-loading");
      try {
        const resp = await fetch(src);
        if (!resp.ok) throw new Error(`HTTP ${resp.status}`);
        const blob = await resp.blob();
        video.src = URL.createObjectURL(blob);
        await new Promise((resolve, reject) => {
          video.addEventListener("canplaythrough", resolve, { once: true });
          video.addEventListener("error", reject, { once: true });
        });
        loaded = true;
        overlay.style.display = "none";
        video.style.display = "";
        video.play();
      } catch (e) {
        console.error("Video load failed:", e);
        hint.textContent = "加载失败，点击重试";
        overlay.classList.remove("is-loading");
      }
    }

    overlay.addEventListener("click", loadAndPlay);
  });
}

