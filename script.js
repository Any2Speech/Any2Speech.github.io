function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function inferMediaKind(item) {
  if (item.kind) {
    return item.kind;
  }

  const ext = item.src.split(".").pop()?.toLowerCase();
  return ["mp4", "mov", "webm"].includes(ext) ? "video" : "audio";
}

function getMimeType(item, kind) {
  if (item.type) {
    return item.type;
  }

  const ext = item.src.split(".").pop()?.toLowerCase();

  if (kind === "video") {
    if (ext === "webm") {
      return "video/webm";
    }
    if (ext === "mov") {
      return "video/quicktime";
    }
    return "video/mp4";
  }

  if (ext === "mp3") {
    return "audio/mpeg";
  }
  if (ext === "ogg") {
    return "audio/ogg";
  }
  if (ext === "aac") {
    return "audio/aac";
  }
  if (ext === "m4a") {
    return "audio/mp4";
  }
  return "audio/wav";
}

